		<p>Un banc de temps és un sistema d'intercanvi de serveis per temps. En ell la unitat d'intercanvi no és els diners habituals sinó una mesura de temps, per exemple el treball per hora. És un sistema d'intercanvi de serveis per serveis o favors per favors. Proposa l'avantatge de fomentar les relacions socials i la igualtat entre diferents estrats econòmics. Es planteja l'ús d'aquest tipus d'economia per solucionar diversos problemes presents en l'economia de mercat, a manera d'economies complementàries o mercats alternatius. Actualment aquests projectes poden ser potenciats amb el suport de la tecnologia de la informació.</p>
